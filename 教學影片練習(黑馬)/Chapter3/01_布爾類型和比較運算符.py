"""
演示布爾類型的定義
以及比較運算符的應用
"""
# 定義變量存儲布爾類型的數據
bool_1 = True
bool_2 = False
print(f"bool_1的內容是:{bool_1}，而他的類型是:{type(bool_1)}")
print(f"bool_2的內容是:{bool_2}，而他的類型是:{type(bool_2)}")
# 比較運算符的使用
# ==, !=,  >,<,>=,<=
# 演示進行內容的相等比較
num_1 = 10
num_2 = 10
print(f"num_1 == num_2，他變量內容是: {num_1 == num_2}，他的類型是:{type(num_1 == num_2)}")

num_1 = 10
num_2 = 15
print(f"num_1 != num_2，他變量內容是: {num_1 != num_2}，他的類型是:{type(num_1 != num_2)}")
