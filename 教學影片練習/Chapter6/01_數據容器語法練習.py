"""
演示列表 list
"""

# 字面量
# ['Hi','Hello','world']
# 塞進變量裡面
"""
list1 = ['Hi','Hello','world']
print(list1)
"""

# 不同變量的時候也可以列印
"""
['hi', True, 123]
<class 'list'>
"""
"""
list2 = ['hi',True,123]
print(list2)
print(type(list2))
"""

# 嵌套
"""
[[1, 2, 3], [4, 5, 6], [7, 8, 9]]
<class 'list'>
"""
list3 = [[1,2,3],[4,5,6],[7,8,9]]
print(list3)
print(type(list3))
